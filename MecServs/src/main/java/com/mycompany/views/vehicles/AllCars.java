    public void loadCars(){
        try{
              DefaultTableModel model = (DefaultTableModel) tableContent.getModel();
              CarroDaoImpl.carrosDisponiveis.forEach((u)->model.addRow(new Object[]{u.getPlaca(),u.getModelo(),u.getAnoFabric(),u.getTipoCombustivel(),u.getNumPortas(),u.getCapacidadePortaMalas()}));
          }catch(Exception e){
              System.out.println("Nao foi possivel listar os veiculos." + e.getMessage());
          }
    }
