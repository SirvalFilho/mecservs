      public static void loadVehicles(){ //NÃ£o esta listando na pagina principal
          try{
              DefaultTableModel model = (DefaultTableModel) tableContent.getModel();
              if( CarroDaoImpl.carrosDisponiveis != null){
                  CarroDaoImpl.carrosDisponiveis.forEach((u)->model.addRow(new Object[]{u.getTipo(),u.getPlaca(),u.getModelo(),u.getAnoFabric()}));
              }
              if(MotoDaoImpl.motosDisponiveis != null){
                  MotoDaoImpl.motosDisponiveis.forEach((u)->model.addRow(new Object[]{u.getTipo(),u.getPlaca(),u.getModelo(),u.getAnoFabric()}));  
              }
              if( UtilDaoImpl.utilsDisponiveis != null){
                   UtilDaoImpl.utilsDisponiveis.forEach((u)->model.addRow(new Object[]{u.getTipo(),u.getPlaca(),u.getModelo(),u.getAnoFabric()}));  
              }  
          
          }catch(Exception e){
              System.out.println("Nao foi possivel listar os veiculos." + e.getMessage());
          }
      }
      
